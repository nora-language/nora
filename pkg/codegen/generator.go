func (g *Generator) generateLambdaFunction(e *ast.LambdaExpression) string {
	oldLambda := g.CurrentLambda
	g.CurrentLambda = e
	oldNoTemp := g.NoTempWrap
	g.NoTempWrap = false
	defer func() {
		g.CurrentLambda = oldLambda
		g.NoTempWrap = oldNoTemp
	}()

	t := g.SemanticInfo.Types[e]
	ft, _ := t.(*types.FunctionType)

	h := sha256.New()
	h.Write([]byte(filepath.Base(e.Pos().Filename)))
	h.Write([]byte(fmt.Sprintf(":%d:%d", e.Pos().Line, e.Pos().Column)))
	if ft != nil {
		h.Write([]byte(fmt.Sprintf("%v", ft)))
	}
	if g.CurrentFunc != nil {
		h.Write([]byte(g.CurrentFunc.Name))
	}
	hashVal := hex.EncodeToString(h.Sum(nil))[:8]
	fnName := fmt.Sprintf("nr_lambda_%s", hashVal)
	if g.GeneratedLambdas[fnName] {
		return fnName
	}
	g.GeneratedLambdas[fnName] = true
	envStructName := fnName + "_env_t"

	scope := g.SemanticInfo.Scopes[e]

	var captures []*semantic.Symbol
	if scope != nil && scope.Captures != nil {
		for sym := range scope.Captures {
			captures = append(captures, sym)
		}
	}

	sort.Slice(captures, func(i, j int) bool {
		return captures[i].Name < captures[j].Name
	})

	var envDef bytes.Buffer
	inHeader := false
	if g.MultiFileMode && g.hirProg != nil {
		for _, hf := range g.hirProg.Functions {
			if hf.Name == fnName {
				inHeader = true
				break
			}
		}
	}
	if !inHeader {
		envDef.WriteString(fmt.Sprintf("typedef struct {\n"))
		for _, cap := range captures {
			envDef.WriteString(fmt.Sprintf("    %s %s;\n", g.cType(cap.Type), g.mangleName(cap)))
		}
		envDef.WriteString(fmt.Sprintf("} %s;\n\n", envStructName))
	}

	// Generate a custom drop function for the environment struct
	envDef.WriteString(fmt.Sprintf("void %s_env_drop(void* env_ptr) {\n", fnName))
	envDef.WriteString(fmt.Sprintf("    if (!env_ptr) return;\n"))
	envDef.WriteString(fmt.Sprintf("    %s* env = (%s*)env_ptr;\n", envStructName, envStructName))

	// Temporarily redirect buffer to envDef to generate recursive drops for captured owned fields
	oldBuf := g.buf
	g.buf = &envDef
	oldDebug := g.EnableDebug
	g.EnableDebug = false
	for _, cap := range captures {
		if types.IsOwnedType(cap.Type) {
			fieldExpr := fmt.Sprintf("env->%s", g.mangleName(cap))
			g.emitDrop(fieldExpr, cap.Type, g.isPointerTypeInC(cap.Type))
		}
	}
	g.EnableDebug = oldDebug
	g.emit("    nr_free(env);\n")
	g.emit("}\n\n")
	g.buf = oldBuf

	g.SpawnStructs = append(g.SpawnStructs, envDef.String())

	var fnDef bytes.Buffer
	t = g.SemanticInfo.Types[e]
	ft = t.(*types.FunctionType)

	fnDef.WriteString(fmt.Sprintf("%s %s(void* _env_ptr", g.cType(ft.Return), fnName))
	for i, p := range ft.Params {
		fnDef.WriteString(", ")
		lease := types.LeaseRead
		if i < len(ft.ParamLeases) {
			lease = ft.ParamLeases[i]
		}
		fnDef.WriteString(g.cParamType(p, lease))
		if i < len(e.Parameters) && e.Parameters[i].Name != nil {
			fnDef.WriteString(" ")
			fnDef.WriteString(e.Parameters[i].Name.Value)
		}
	}
	fnDef.WriteString(") {\n")
	fnDef.WriteString(fmt.Sprintf("    %s* _env = (%s*)_env_ptr;\n", envStructName, envStructName))

	oldBuf = g.buf
	g.buf = &fnDef

	for _, param := range e.Parameters {
		if param.Name != nil {
			sym := g.SemanticInfo.Defs[param.Name]
			if sym == nil {
				sym = g.SemanticInfo.Uses[param.Name]
			}
			if sym != nil && g.hasDropFlag(sym) {
				g.emit("    bool %s = true;", g.dropFlagName(sym))
			}
		}
	}

	fakeSym := &semantic.Symbol{
		Name: fnName,
		Type: ft,
	}
	oldFunc := g.CurrentFunc
	g.CurrentFunc = fakeSym

	oldBlock := g.CurrentBlock
	oldIdx := g.CurrentStmtIndex
	g.CurrentBlock = e.Body

	g.emitDropsAt(e.Body, 0)
	for i, stmt := range e.Body.Statements {
		g.CurrentStmtIndex = i
		g.emitPreDropsAt(e.Body, i)
		g.genStatement(stmt)
		g.emitDropsAt(e.Body, i+1)
	}

	g.CurrentBlock = oldBlock
	g.CurrentStmtIndex = oldIdx
	g.CurrentFunc = oldFunc
	g.buf = oldBuf

	fnDef.WriteString("}\n")

	g.SpawnWrappers = append(g.SpawnWrappers, fnDef.String())

	return fnName
}

func (g *Generator) populateLambdaEnv(e *ast.LambdaExpression, scope *semantic.Scope) {
	var captures []*semantic.Symbol
	if scope != nil && scope.Captures != nil {
		for sym := range scope.Captures {
			captures = append(captures, sym)
		}
	}
	sort.Slice(captures, func(i, j int) bool {
		return captures[i].Name < captures[j].Name
	})

	for _, cap := range captures {
		name := g.mangleName(cap)
		rhs := name
		if g.CurrentLambda != nil {
			parentScope := g.SemanticInfo.Scopes[g.CurrentLambda]
			if parentScope != nil && parentScope.Captures != nil && parentScope.Captures[cap] {
				rhs = fmt.Sprintf("_env->%s", name)
			}
		}
		g.buf.WriteString(fmt.Sprintf("_env_local->%s = %s; ", name, rhs))
	}
}

func (g *Generator) getHIRFunctionPackage(hf *hir.Function) string {
	if hf.FuncSymbol != nil {
		if pkg := g.getSymbolPackage(hf.FuncSymbol); pkg != "" {
			return pkg
		}
	}
	if hf.LambdaExpr != nil {
		filename, err1 := filepath.Abs(hf.LambdaExpr.Pos().Filename)
		for _, file := range g.Program.Files {
			absName, err2 := filepath.Abs(file.Name)
			if err1 == nil && err2 == nil && absName == filename {
				return g.getPackageName(file)
			}
		}
	}
	return ""
}

func (g *Generator) emitLambdaEnvDefs() {
	if g.hirProg == nil {
		return
	}
	for _, hf := range g.hirProg.Functions {
		if hf.LambdaExpr != nil {
			fnName := hf.Name
			envStructName := fnName + "_env_t"
			scope := g.SemanticInfo.Scopes[hf.LambdaExpr]
			hasCaptures := false
			if scope != nil && scope.Captures != nil && len(scope.Captures) > 0 {
				hasCaptures = true
			}
			if hasCaptures {
				var captures []*semantic.Symbol
				for sym := range scope.Captures {
					captures = append(captures, sym)
				}
				sort.Slice(captures, func(i, j int) bool {
					return captures[i].Name < captures[j].Name
				})

				g.emit(fmt.Sprintf("typedef struct {"))
				for _, cap := range captures {
					g.emit(fmt.Sprintf("    %s %s;", g.cType(cap.Type), g.mangleName(cap)))
				}
				g.emit(fmt.Sprintf("} %s;", envStructName))
				g.emit(fmt.Sprintf("void %s_env_drop(void* env_ptr);", fnName))
				g.emit("")
			}
		}
	}
}

