type Generator struct {
	Program          *ast.Program
	SemanticInfo     *semantic.SemanticInfo
	Solver           *topology.Solver
	PluginMgr        *plugin.PluginManager
	Target           string
	EnableDebug      bool
	DebugMemory      bool
	DebugSemantic    bool
	NoStdlib         bool
	NoTempWrap       bool
	InMoveOperator   bool
	InCallExpression bool
	MultiFileMode    bool
	TargetIsValue    bool

	// Internal State
	buf             *bytes.Buffer
	Functions       map[string]*semantic.Symbol
	Structs         map[string]*types.StructType
	SumTypes        map[string]*types.SumType
	Protocols       map[string]*types.ProtocolType
	Globals         map[string]*semantic.Symbol
	GlobalInits     []*ast.VarStatement
	SpawnWrappers   []string
	SpawnStructs    []string
	spawnCounter    int
	ScopeWaitgroups []string
	scopeCounter    int

	// Loop states
	loopCounter int

	currentFile string
	currentLine int

	CurrentFunc     *semantic.Symbol
	CurrentLambda   *ast.LambdaExpression
	AutoDropMethods map[string]types.NRType
	AutoEqMethods   map[string]types.NRType
	VTables         map[string]VTableInstance

	CurrentBlock     *ast.BlockStatement
	CurrentStmtIndex int

	eraseCache       map[string]types.NRType
	NativeHeaders    []string
	GeneratedLambdas map[string]bool

	// String Literals
	StringLiterals map[string]string // maps string content to global variable name
	hirProg        *hir.Program
}

type VTableInstance struct {
	Type     types.NRType
	Protocol *types.ProtocolType
}

func NewGenerator(prog *ast.Program, sem *semantic.SemanticInfo, solver *topology.Solver, pluginMgr *plugin.PluginManager, nativeHeaders []string) *Generator {
	return &Generator{
		Program:          prog,
		SemanticInfo:     sem,
		Solver:           solver,
		PluginMgr:        pluginMgr,
		Functions:        make(map[string]*semantic.Symbol),
		Structs:          make(map[string]*types.StructType),
		SumTypes:         make(map[string]*types.SumType),
		Protocols:        make(map[string]*types.ProtocolType),
		Globals:          make(map[string]*semantic.Symbol),
		buf:              new(bytes.Buffer),
		currentLine:      -1,
		AutoDropMethods:  make(map[string]types.NRType),
		AutoEqMethods:    make(map[string]types.NRType),
		VTables:          make(map[string]VTableInstance),
		eraseCache:       make(map[string]types.NRType),
		NativeHeaders:    nativeHeaders,
		StringLiterals:   make(map[string]string),
		GeneratedLambdas: make(map[string]bool),
	}
}

func (g *Generator) emitRuntime() {
	if g.NoStdlib {
		g.emit("#include <stdint.h>")
		g.emit("#include <stdbool.h>")
		g.emit("#include <stddef.h>")
		g.emit("")
	} else {
		for _, header := range g.NativeHeaders {
			g.emit(fmt.Sprintf("#include \"%s\"", header))
		}
		g.emit("")
	}

	if g.DebugMemory {
		g.emit("#define NR_DEBUG_MEM 1")
	} else {
		g.emit("#define NR_DEBUG_MEM 0")
	}
	g.emit("")
	g.emit("#define malloc nr_malloc")
	g.emit("#define free nr_free")
	g.emit("#define strdup nr_strdup")
}

func (g *Generator) emitYieldCheckpoint() {
	if !g.NoStdlib {
		g.emit("    NR_COOPERATIVE_YIELD_CHECKPOINT();")
	}
}

func (g *Generator) Generate() (string, error) {
	g.buf.Reset()
	// 1. Collect all definitions
	g.collectDefinitions()

	// Lower AST to HIR
	if g.hirProg == nil {
		lowerer := hir.NewLowerer(g.SemanticInfo, g.Solver)
		lowerer.DebugLog = g.DebugSemantic
		prog := lowerer.LowerProgram(g.Program)
		opt := optimize.NewOptimizer(g.SemanticInfo)
		g.hirProg = opt.OptimizeProgram(prog)
	}
	hirProg := g.hirProg
	hirFuncs := make(map[string]*hir.Function)
	for _, hf := range hirProg.Functions {
		hirFuncs[g.mangleName(hf.FuncSymbol)] = hf
	}

	// 2. Emit Header
	g.emit("// Generated by Nora Compiler")
	g.emit("")

	// Emit native headers requested by dependencies
	for _, hdr := range g.NativeHeaders {
		g.emit(fmt.Sprintf("#include \"%s\"", hdr))
	}
	g.emit("")

	// 4. Emit Runtime Components
	g.emitRuntime()

	// 5. Emit Type Forward Declarations
	g.emitForwardDeclarations()

	// 7. Emit Protocol Definitions
	g.emitProtocolDefs()

	// 6. Emit Topologically Sorted Type Definitions (Combined Struct and Sum Types)
	g.emitCombinedTypeDefs()

	// Emit Lambda Environment Struct Definitions
	g.emitLambdaEnvDefs()

	// 8. Emit Prototypes
	for _, file := range g.Program.Files {
		g.emitExternPrototypes(file)
	}
	g.emitPrototypes()

	// 9. Emit Global Definitions
	g.emitGlobalDefs()

	// 10. Generate Function Definitions (but don't emit yet)
	funcBuf := new(bytes.Buffer)
	mainBuf := g.buf
	g.buf = funcBuf

	for _, name := range g.sortedFunctionNames() {
		sym := g.Functions[name]
		if sym.DefNode == nil {
			continue
		}
		fn, ok := sym.DefNode.(*ast.FunctionStatement)
		if !ok || ast.GetAttribute(fn.Attributes, "macro") != nil || fn.Body == nil {
			continue
		}
		if hf, ok := hirFuncs[name]; ok {
			g.genHIRFunction(hf)
		} else {
			if fnStmt, ok := sym.DefNode.(*ast.FunctionStatement); ok && len(fnStmt.TypeParameters) > 0 {
				// 
			}
			g.genFunction(sym, fn)
		}
	}

	// Compile lowered lambda functions
	for _, hf := range hirProg.Functions {
		if hf.LambdaExpr != nil {
			g.genHIRFunction(hf)
		}
	}
	// 11b. Emit Auto-Generated Drop Methods (to handle recursive structures)
	g.emitAutoDropMethods()

	// Emit Lambda Environment Drop Functions
	g.emitLambdaEnvDrops()

	// 11d. Emit Auto-Generated Equality Methods
	g.emitAutoEqMethods()

	// 11c. Emit Global Initializers
	g.emitGlobalInits()

	// 12. Emit Main Wrapper
	mainName := "main" // Default Nora main
	for _, sym := range g.Functions {
		if sym.Name == "main" {
			mainName = g.mangleName(sym)
			break
		}
	}
	platform := g.GetTarget()
	g.emit(platform.GetMainWrapper(mainName))

	// Restore main buffer
	g.buf = mainBuf

	// [NEW] Now that ALL generations have finished, g.StringLiterals is 100% complete!
	// We can safely emit all collected string literals here:
	g.emitStringLiterals()

	// [NEW] Emit Prototypes for Auto-Generated Drop Methods
	g.emitAutoDropPrototypes()
	// [NEW] Emit Prototypes for Auto-Generated Equality Methods
	g.emitAutoEqPrototypes()

	g.emitVTables()

	// Emit Global Cleanups
	g.emitGlobalCleanups()

	// 9b. Emit Spawn Wrappers (now populated)
	for _, s := range g.SpawnStructs {
		g.emit(s)
	}
	for _, s := range g.SpawnWrappers {
		g.emit(s)
	}

	// Finally, append all the compiled functions, drop methods, global inits, and main wrapper!
	g.buf.Write(funcBuf.Bytes())

	return g.buf.String(), nil
}

func (g *Generator) eraseType(t types.NRType) types.NRType {
	if t == nil {
		return nil
	}
	if t.Name() == "str" || t.Name() == "ptr" {
		return types.Ptr
	}
	switch pt := t.(type) {
	case *types.PointerType:
		erasedBase := g.eraseType(pt.Base)
		if st, ok := erasedBase.(*types.StructType); ok && st.BaseType != nil {
			return &types.PointerType{
				Base:    erasedBase,
				IsArray: pt.IsArray,
				Leased:  pt.Leased,
				Kind:    pt.Kind,
			}
		}
		if sum, ok := erasedBase.(*types.SumType); ok && sum.BaseType != nil {
			return &types.PointerType{
				Base:    erasedBase,
				IsArray: pt.IsArray,
				Leased:  pt.Leased,
				Kind:    pt.Kind,
			}
		}
		if fn, ok := erasedBase.(*types.FunctionType); ok {
			return &types.PointerType{
				Base:    fn,
				IsArray: pt.IsArray,
				Leased:  pt.Leased,
				Kind:    pt.Kind,
			}
		}
		return types.Ptr
	case *types.ListType:
		return &types.ListType{
			ElementType: g.eraseType(pt.ElementType),
		}
	case *types.ChanType:
		return &types.ChanType{
			Elem: g.eraseType(pt.Elem),
		}
	case *types.StructType:
		if pt.BaseType != nil && len(pt.TypeArgs) > 0 {
			allPointerLike := true
			for _, arg := range pt.TypeArgs {
				if !types.IsPointerLike(arg) || types.IsOwnedType(arg) {
					allPointerLike = false
					break
				}
			}
			if allPointerLike {
				if erasedName := g.getErasedTypeName(pt); erasedName != "" {
					if cached, ok := g.eraseCache[erasedName]; ok {
						return cached
					}

					erasedArgs := make([]types.NRType, len(pt.TypeArgs))
					for i := range pt.TypeArgs {
						erasedArgs[i] = types.Ptr
					}
					placeholder := &types.StructType{
						TypeName:   erasedName,
						BaseType:   pt.BaseType,
						TypeParams: pt.TypeParams,
						TypeArgs:   erasedArgs,
						Methods:    make(map[string]types.NRType),
						FieldNames: append([]string(nil), pt.FieldNames...),
					}
					g.eraseCache[erasedName] = placeholder

					erasedFields := make(map[string]types.NRType)
					placeholder.Fields = erasedFields
					for fName, fType := range pt.Fields {
						erasedFields[fName] = g.eraseType(fType)
					}
					return placeholder
				}
			}
		}
	case *types.SumType:
		if pt.BaseType != nil && len(pt.TypeArgs) > 0 {
			allPointerLike := true
			for _, arg := range pt.TypeArgs {
				if !types.IsPointerLike(arg) || types.IsOwnedType(arg) {
					allPointerLike = false
					break
				}
			}
			if allPointerLike {
				if erasedName := g.getErasedTypeName(pt); erasedName != "" {
					if cached, ok := g.eraseCache[erasedName]; ok {
						return cached
					}

					erasedArgs := make([]types.NRType, len(pt.TypeArgs))
					for i := range pt.TypeArgs {
						erasedArgs[i] = types.Ptr
					}
					placeholder := &types.SumType{
						TypeName:   erasedName,
						BaseType:   pt.BaseType,
						TypeParams: pt.TypeParams,
						TypeArgs:   erasedArgs,
						Methods:    make(map[string]types.NRType),
					}
					g.eraseCache[erasedName] = placeholder

					erasedVariants := make(map[string]*types.Variant)
					placeholder.Variants = erasedVariants
					for vName, variant := range pt.Variants {
						erasedVar := &types.Variant{
							Name:       variant.Name,
							Tag:        variant.Tag,
							Fields:     make(map[string]types.NRType),
							FieldNames: append([]string(nil), variant.FieldNames...),
						}
						for fName, fType := range variant.Fields {
							erasedVar.Fields[fName] = g.eraseType(fType)
						}
						erasedVariants[vName] = erasedVar
					}
					return placeholder
				}
			}
		}
	}
	return t
}

func (g *Generator) isTypeNamePointerLike(name string) bool {
	name = strings.TrimPrefix(name, "@")
	name = strings.TrimPrefix(name, "#")
	name = strings.TrimPrefix(name, "&")
	name = strings.TrimPrefix(name, "*")
	return name == "ptr" || name == "str" || strings.HasSuffix(name, "*") || strings.HasPrefix(name, "ptr") || strings.HasPrefix(name, "str") || strings.Contains(name, "_ptr") || strings.Contains(name, "_str") || strings.Contains(name, "ptr_") || strings.Contains(name, "str_")
}

func (g *Generator) findDefSymbol(name string) *semantic.Symbol {
	for id, sym := range g.SemanticInfo.Defs {
		if id != nil && id.Value == name {
			return sym
		}
		if sym != nil && sym.Name == name {
			return sym
		}
	}
	for _, subMap := range g.SemanticInfo.MethodSymbols {
		for _, sym := range subMap {
			if sym != nil && sym.Name == name {
				return sym
			}
		}
	}
	return nil
}

func (g *Generator) collectDefinitions() {
	for _, file := range g.Program.Files {
		for _, stmt := range file.Statements {
			switch s := stmt.(type) {
			case *ast.FunctionStatement:
				if len(s.TypeParameters) > 0 || s.IsGenericTemplate {
					continue
				}
				sym := g.SemanticInfo.Defs[s.Name]
				if sym != nil {
					g.Functions[g.mangleName(sym)] = sym
				}
			case *ast.TypeStatement:
				if len(s.TypeParameters) > 0 {
					continue
				}
				sym := g.SemanticInfo.Defs[s.Name]
				if sym == nil {
					if g.DebugSemantic {
						fmt.Printf("[DEBUG-collectDefinitions] Name=%s NOT FOUND\n", s.Name.Value)
					}
					continue
				}
				t := sym.Type
				if g.DebugSemantic {
					fmt.Printf("[DEBUG-collectDefinitions] Name=%s, Type=%T\n", s.Name.Value, t)
				}
				mangled := g.mangleName(sym)
				if st, ok := t.(*types.StructType); ok {
					if len(st.TypeParams) > 0 {
						continue
					}
					g.Structs[mangled] = st
					if g.getDropMethod(st) == "" && g.hasOwnedFields(st) {
						g.requestAutoDrop(st)
					}
					g.getEqMethod(st)
				} else if sum, ok := t.(*types.SumType); ok {
					if len(sum.TypeParams) > 0 {
						continue
					}
					g.SumTypes[mangled] = sum
					if g.getDropMethod(sum) == "" && g.hasOwnedSumFields(sum) {
						g.requestAutoDrop(sum)
					}
				} else if proto, ok := t.(*types.ProtocolType); ok {
					g.Protocols[mangled] = proto
				}
			case *ast.VarStatement:
				sym := g.SemanticInfo.Defs[s.Name]
				if sym != nil && sym.DefScope != nil && sym.DefScope.Kind == semantic.ScopePackage {
					g.Globals[g.mangleName(sym)] = sym
					if s.Value != nil {
						g.GlobalInits = append(g.GlobalInits, s)
					}
				}
			}
		}
	}

	// 1. Collect specialized types (monomorphized structs/sums) WITH type erasure
	for mangled, t := range g.SemanticInfo.SpecTypes {
		if g.isGeneric(t) {
			continue
		}
		if st, ok := t.(*types.StructType); ok {
			erasedMangled := g.getErasedTypeName(st)
			if erasedMangled != "" {
				// Check if already registered
				if _, exists := g.Structs[erasedMangled]; !exists {
					erasedSt := types.NewStructType(erasedMangled)
					erasedSt.BaseType = st.BaseType
					erasedSt.TypeArgs = make([]types.NRType, len(st.TypeArgs))
					for i := range st.TypeArgs {
						erasedSt.TypeArgs[i] = types.Ptr
					}
					erasedSt.FieldNames = append([]string(nil), st.FieldNames...)
					// Erase fields
					for fName, fType := range st.Fields {
						erasedSt.Fields[fName] = g.eraseType(fType)
					}
					g.Structs[erasedMangled] = erasedSt
					if g.getDropMethod(erasedSt) == "" && g.hasOwnedFields(erasedSt) {
						g.requestAutoDrop(erasedSt)
					}
					g.getEqMethod(erasedSt)
				}
			} else {
				g.Structs[mangled] = st
				if g.getDropMethod(st) == "" && g.hasOwnedFields(st) {
					g.requestAutoDrop(st)
				}
				g.getEqMethod(st)
			}
		} else if sum, ok := t.(*types.SumType); ok {
			erasedMangled := g.getErasedTypeName(sum)
			if erasedMangled != "" {
				if _, exists := g.SumTypes[erasedMangled]; !exists {
					erasedSum := &types.SumType{
						TypeName: erasedMangled,
						Variants: make(map[string]*types.Variant),
						Methods:  make(map[string]types.NRType),
					}
					erasedSum.BaseType = sum.BaseType
					erasedSum.TypeArgs = make([]types.NRType, len(sum.TypeArgs))
					for i := range sum.TypeArgs {
						erasedSum.TypeArgs[i] = types.Ptr
					}
					// Erase variants
					for vName, variant := range sum.Variants {
						erasedVar := &types.Variant{
							Name:       variant.Name,
							Tag:        variant.Tag,
							Fields:     make(map[string]types.NRType),
							FieldNames: append([]string(nil), variant.FieldNames...),
						}
						for fName, fType := range variant.Fields {
							erasedVar.Fields[fName] = g.eraseType(fType)
						}
						erasedSum.Variants[vName] = erasedVar
					}
					g.SumTypes[erasedMangled] = erasedSum
					if g.getDropMethod(erasedSum) == "" && g.hasOwnedSumFields(erasedSum) {
						g.requestAutoDrop(erasedSum)
					}
				}
			} else {
				g.SumTypes[mangled] = sum
				if g.getDropMethod(sum) == "" && g.hasOwnedSumFields(sum) {
					g.requestAutoDrop(sum)
				}
			}
		}
	}

	// 2. Collect monomorphized function instances WITH type erasure
	for fnStmt, instMap := range g.SemanticInfo.Instances {
		for typeKey, inst := range instMap {
			if inst.IsGenericTemplate {
				continue
			}
			sym := g.SemanticInfo.Defs[inst.Name]
			if sym == nil {
				sym = g.findDefSymbol(inst.Name.Value)
			}
			if sym == nil {
				continue
			}

			// Check if all type arguments are pointer-like
			parts := strings.Split(typeKey, "_")
			allPointerLike := true
			hasParts := false
			for _, part := range parts {
				if part == "" {
					continue
				}
				hasParts = true
				if !g.isTypeNamePointerLike(part) {
					allPointerLike = false
					break
				}
			}

			// Also check if the receiver is pointer-like
			ft, ok := sym.Type.(*types.FunctionType)
			if ok && ft.Receiver != nil {
				if g.getErasedTypeName(ft.Receiver) != "" {
					allPointerLike = true
					hasParts = true
				}
			}

			if hasParts && allPointerLike {
				// Type-erased shared monomorphization!
				// Compute erased mangled name for the function
				erasedName := g.mangleName(sym)
				if ft != nil && ft.Receiver != nil {
					erasedRec := g.getErasedTypeName(ft.Receiver)
					if erasedRec != "" {
						methodBase := inst.Name.Value
						if len(methodBase) > 9 && methodBase[len(methodBase)-9] == '_' {
							isHex := true
							for _, c := range methodBase[len(methodBase)-8:] {
								if !((c >= '0' && c <= '9') || (c >= 'a' && c <= 'f')) {
									isHex = false
									break
								}
							}
							if isHex {
								methodBase = methodBase[:len(methodBase)-9]
							}
						}
						erasedName = erasedRec + "_" + methodBase
					}
				} else {
					pkgName := g.getSymbolPackage(sym)
					baseMangledName := fnStmt.Name.Value
					if pkgName != "" && pkgName != "main" {
						safePkg := strings.ReplaceAll(pkgName, "/", "_")
						safePkg = strings.ReplaceAll(safePkg, ".", "_")
						baseMangledName = safePkg + "_" + fnStmt.Name.Value
					}
					erasedName = baseMangledName + "_ptr"
				}

				if _, exists := g.Functions[erasedName]; !exists {
					erasedFT := &types.FunctionType{
						Params:        make([]types.NRType, len(ft.Params)),
						ParamLeases:   ft.ParamLeases,
						Return:        ft.Return,
						IsMethod:      ft.IsMethod,
						Receiver:      ft.Receiver,
						ReceiverLease: ft.ReceiverLease,
					}
					if ft.Receiver != nil {
						if erasedRecT := g.Structs[g.getErasedTypeName(ft.Receiver)]; erasedRecT != nil {
							erasedFT.Receiver = erasedRecT
						} else if erasedRecSum := g.SumTypes[g.getErasedTypeName(ft.Receiver)]; erasedRecSum != nil {
							erasedFT.Receiver = erasedRecSum
						}
					}
					for i, param := range ft.Params {
						erasedParam := g.eraseType(param)
						if types.IsPointerLike(param) {
							if pt, ok := erasedParam.(*types.PointerType); ok {
								if _, ok := pt.Base.(*types.StructType); ok {
									// Keep structure pointer
								} else if _, ok := pt.Base.(*types.SumType); ok {
									// Keep sum type pointer
								} else if _, ok := pt.Base.(*types.FunctionType); ok {
									// Keep function type lease
								} else {
									erasedParam = types.Ptr
								}
							} else {
								erasedParam = types.Ptr
							}
						}
						erasedFT.Params[i] = erasedParam
					}
					erasedReturn := g.eraseType(ft.Return)
					if types.IsPointerLike(ft.Return) {
						if pt, ok := erasedReturn.(*types.PointerType); ok {
							if _, ok := pt.Base.(*types.StructType); ok {
								// Keep structure pointer
							} else if _, ok := pt.Base.(*types.SumType); ok {
								// Keep sum type pointer
							} else if _, ok := pt.Base.(*types.FunctionType); ok {
								// Keep function type lease
							} else {
								erasedReturn = types.Ptr
							}
						} else {
							erasedReturn = types.Ptr
						}
					}
					erasedFT.Return = erasedReturn

					erasedSym := &semantic.Symbol{
						Name:     erasedName,
						Kind:     sym.Kind,
						Type:     erasedFT,
						DefNode:  sym.DefNode,
						DefScope: sym.DefScope,
					}
					g.Functions[erasedName] = erasedSym
				}
			} else {
				g.Functions[g.mangleName(sym)] = sym
			}
		}
	}

	// 3. Update MonomorphizedNames mapping to point to the erased name for erased function calls
	for e, name := range g.SemanticInfo.MonomorphizedNames {
		if ft, ok := g.SemanticInfo.Types[e.Function].(*types.FunctionType); ok {
			if ft.Receiver != nil {
				erasedRec := g.getErasedTypeName(ft.Receiver)
				if erasedRec != "" {
					if sel, ok := e.Function.(*ast.SelectorExpression); ok {
						methodBase := sel.Field.Value
						g.SemanticInfo.MonomorphizedNames[e] = erasedRec + "_" + methodBase
					}
				} else {
					if _, ok := e.Function.(*ast.SelectorExpression); ok {
						receiverType := ft.Receiver
						underlying := receiverType
						for {
							if pt, ok := underlying.(*types.PointerType); ok {
								underlying = pt.Base
								continue
							}
							break
						}

						pkgPrefix := ""
						for mangled, st := range g.Structs {
							if st == underlying || (st.BaseType != nil && st.BaseType == underlying) {
								if idx := strings.Index(mangled, "_"); idx != -1 {
									pkgPrefix = mangled[:idx]
									break
								}
							}
						}
						if pkgPrefix == "" {
							for mangled, sum := range g.SumTypes {
								if sum == underlying || (sum.BaseType != nil && sum.BaseType == underlying) {
									if idx := strings.Index(mangled, "_"); idx != -1 {
										pkgPrefix = mangled[:idx]
										break
									}
								}
							}
						}

						if pkgPrefix != "" && pkgPrefix != "main" && !strings.HasPrefix(name, pkgPrefix+"_") {
							g.SemanticInfo.MonomorphizedNames[e] = pkgPrefix + "_" + name
						}
					}
				}
			} else {
				for fnStmt, instMap := range g.SemanticInfo.Instances {
					for typeKey, inst := range instMap {
						sym := g.SemanticInfo.Defs[inst.Name]
						if sym == nil {
							sym = g.findDefSymbol(inst.Name.Value)
						}
						if sym != nil && g.mangleName(sym) == name {
							parts := strings.Split(typeKey, "_")
							allPointerLike := true
							hasParts := false
							for _, part := range parts {
								if part == "" {
									continue
								}
								hasParts = true
								if !g.isTypeNamePointerLike(part) {
									allPointerLike = false
									break
								}
							}
							if hasParts && allPointerLike {
								pkgName := g.getSymbolPackage(sym)
								baseMangledName := fnStmt.Name.Value
								if pkgName != "" && pkgName != "main" {
									safePkg := strings.ReplaceAll(pkgName, "/", "_")
									safePkg = strings.ReplaceAll(safePkg, ".", "_")
									baseMangledName = safePkg + "_" + fnStmt.Name.Value
								}
								g.SemanticInfo.MonomorphizedNames[e] = baseMangledName + "_ptr"
							}
						}
					}
				}
			}
		}
	}

	if g.hirProg == nil {
		lowerer := hir.NewLowerer(g.SemanticInfo, g.Solver)
		lowerer.DebugLog = g.DebugSemantic
		prog := lowerer.LowerProgram(g.Program)
		opt := optimize.NewOptimizer(g.SemanticInfo)
		g.hirProg = opt.OptimizeProgram(prog)
	}
}

func (g *Generator) hasOwnedFields(st *types.StructType) bool {
	for _, fType := range st.Fields {
		if types.IsOwnedType(fType) {
			return true
		}
	}
	return false
}

func (g *Generator) hasOwnedSumFields(st *types.SumType) bool {
	for _, variant := range st.Variants {
		for _, fType := range variant.Fields {
			if types.IsOwnedType(fType) {
				return true
			}
		}
	}
	return false
}

