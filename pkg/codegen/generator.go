func (g *Generator) isGeneric(t types.NRType) bool {
	if t == nil {
		return false
	}
	if t.GetKind() == types.KindGeneric {
		return true
	}
	if st, ok := t.(*types.StructType); ok {
		if len(st.TypeParams) > 0 {
			return true
		}
		for _, arg := range st.TypeArgs {
			if g.isGeneric(arg) {
				return true
			}
		}
	}
	if sum, ok := t.(*types.SumType); ok {
		if len(sum.TypeParams) > 0 {
			return true
		}
		for _, arg := range sum.TypeArgs {
			if g.isGeneric(arg) {
				return true
			}
		}
	}
	if pt, ok := t.(*types.PointerType); ok {
		return g.isGeneric(pt.Base)
	}
	return false
}

func (g *Generator) genFunction(sym *semantic.Symbol, fn *ast.FunctionStatement) {
	ft := sym.Type.(*types.FunctionType)
	retType := g.cType(ft.Return)
	name := g.mangleName(sym)

	var params string
	if !fn.IsExport {
		params = "void* _env_ptr"
	}

	if fn.Receiver != nil {
		t := g.cParamType(ft.Receiver, ft.ReceiverLease)
		if params != "" {
			params += ", "
		}
		params += t + " " + fn.Receiver.Name.Value
	}
	for i, p := range ft.Params {
		if params != "" {
			params += ", "
		}
		lease := types.LeaseRead
		if i < len(ft.ParamLeases) {
			lease = ft.ParamLeases[i]
		}
		t := g.cParamType(p, lease)
		params += t
		if i < len(fn.Parameters) && fn.Parameters[i].Name != nil {
			params += " " + fn.Parameters[i].Name.Value
		}
	}

	if fn.Body == nil {
		g.emit(";")
		return
	}

	g.CurrentFunc = sym
	g.emitLine(fn)
	g.emit("%s %s(%s) ", retType, name, params)

	// Open block
	g.emit("{")
	g.emitYieldCheckpoint()

	if fn.IsExport {
		g.emit("    void* _env_ptr = NULL;")
	}

	// [NEW] Add NULL check for drop methods to prevent segfaults
	if fn.Receiver != nil && strings.HasPrefix(fn.Name.Value, "drop") {
		g.emit("if (%s == NULL) return;", fn.Receiver.Name.Value)
	}

	if fn.Receiver != nil {
		sym := g.SemanticInfo.Defs[fn.Receiver.Name]
		if sym == nil {
			sym = g.SemanticInfo.Uses[fn.Receiver.Name]
		}
		if sym != nil && g.hasDropFlag(sym) {
			g.emit("    bool %s = true;", g.dropFlagName(sym))
		}
	}
	for _, param := range fn.Parameters {
		sym := g.SemanticInfo.Defs[param.Name]
		if sym == nil {
			sym = g.SemanticInfo.Uses[param.Name]
		}
		if sym != nil && g.hasDropFlag(sym) {
			g.emit("    bool %s = true;", g.dropFlagName(sym))
		}
	}

	oldBlock := g.CurrentBlock
	oldIdx := g.CurrentStmtIndex
	g.CurrentBlock = fn.Body

	g.emitDropsAt(fn.Body, 0)
	for i, stmt := range fn.Body.Statements {
		g.CurrentStmtIndex = i
		g.emitPreDropsAt(fn.Body, i)
		g.genStatement(stmt)
		g.emitDropsAt(fn.Body, i+1)
	}

	g.CurrentBlock = oldBlock
	g.CurrentStmtIndex = oldIdx

	// [NEW] If this is a 'drop' method and the receiver is owned (@T),
	// we MUST free the receiver itself at the very end.
	if fn.Receiver != nil && strings.HasPrefix(fn.Name.Value, "drop") {
		// [NEW] Drop owned fields of the struct automatically.
		// This ensures recursive drops for linked structures.
		receiverBase := types.UnwrapLease(ft.Receiver)
		// Special case: UnwrapLease doesn't unwrap @ (LeaseMove), but we need it here
		if pt, ok := ft.Receiver.(*types.PointerType); ok && pt.Kind == types.LeaseMove {
			receiverBase = pt.Base
		}

		if st, ok := receiverBase.(*types.StructType); ok {
			for _, fieldName := range st.FieldNames {
				fieldType := st.Fields[fieldName]
				g.emitDrop(fmt.Sprintf("%s->%s", fn.Receiver.Name.Value, fieldName), fieldType, true)
			}
		}

		if g.isHeapAllocated(ft.Receiver) {
			g.emit("    nr_free(%s);", fn.Receiver.Name.Value)
		}
	}

	g.emit("}")
	g.emit("")
	g.CurrentFunc = nil
	g.currentFile = ""
	g.currentLine = -1
}

func (g *Generator) emitDrop(expr string, t types.NRType, isPointerInC bool) {
	if t == nil {
		return
	}
	if !types.IsOwnedType(t) {
		return
	}

	ut := types.UnwrapLease(t)
	if pt, ok := ut.(*types.PointerType); ok && !pt.IsArray {
		ut = pt.Base
	}

	// Handle strings early
	if ut.Name() == "str" {
		g.emit("nr_free_str(%s);", expr)
		return
	}

	isList := ut.GetKind() == types.KindList

	pt, isPointer := t.(*types.PointerType)
	// Case 1: Array/List Drop
	if (isPointer && pt.IsArray) || isList {
		var elemType types.NRType
		if lt, ok := ut.(*types.ListType); ok {
			elemType = lt.ElementType
		} else if isPointer && pt.IsArray {
			elemType = pt.Base
		}

		if elemType != nil && types.IsOwnedType(elemType) {
			g.emit("{")
			g.emit("    int _len = array_count(%s);", expr)
			g.emit("    for (int _i = 0; _i < _len; _i++) {")
			elemExpr := fmt.Sprintf("((%s*)array_data(%s))[_i]", g.cType(elemType), expr)
			g.emitDrop(elemExpr, elemType, g.isPointerTypeInC(elemType))
			g.emit("    }")
			g.emit("}")
		}
		// Fall through to free the array itself if it's heap-allocated
	} else if st, ok := ut.(*types.StructType); ok {
		// Case 2: Struct Drop (Recursive-safe via function call)
		dropMethod := g.getDropMethod(t)
		if dropMethod == "" && g.hasOwnedFields(st) {
			dropMethod = g.requestAutoDrop(st)
		}

		if dropMethod != "" {
			_, isAuto := g.AutoDropMethods[dropMethod]
			if isPointerInC {
				if isAuto {
					g.emit("%s(%s);", dropMethod, expr)
				} else {
					g.emit("%s(NULL, %s);", dropMethod, expr)
				}
			} else {
				if isAuto {
					g.emit("%s(&%s);", dropMethod, expr)
				} else {
					g.emit("%s(NULL, &%s);", dropMethod, expr)
				}
			}
		}
	} else if st, ok := ut.(*types.SumType); ok {
		// Case 3: Sum Type Drop (Recursive-safe via function call)
		dropMethod := g.getDropMethod(t)
		if dropMethod == "" && g.hasOwnedSumFields(st) {
			dropMethod = g.requestAutoDrop(st)
		}

		if dropMethod != "" {
			_, isAuto := g.AutoDropMethods[dropMethod]
			if isPointerInC {
				if isAuto {
					g.emit("%s(%s);", dropMethod, expr)
				} else {
					g.emit("%s(NULL, %s);", dropMethod, expr)
				}
			} else {
				if isAuto {
					g.emit("%s(&%s);", dropMethod, expr)
				} else {
					g.emit("%s(NULL, &%s);", dropMethod, expr)
				}
			}
		}
	} else if _, ok := ut.(*types.ProtocolType); ok {
		// Case 3.5: Protocol (Interface) Drop
		// vtable[0] contains the drop function pointer for the boxed concrete data.
		if isPointerInC {
			g.emit("if (%s && %s->vtable && ((void**)%s->vtable)[0] != NULL) {", expr, expr, expr)
			g.emit("    ((void(*)(void*))((void**)%s->vtable)[0])(%s->data);", expr, expr)
			g.emit("}")
			g.emit("if (%s && %s->data) nr_free(%s->data);", expr, expr, expr)
		} else {
			g.emit("if (%s.vtable && ((void**)%s.vtable)[0] != NULL) {", expr, expr)
			g.emit("    ((void(*)(void*))((void**)%s.vtable)[0])(%s.data);", expr, expr)
			g.emit("}")
			g.emit("if (%s.data) nr_free(%s.data);", expr, expr)
		}
	} else if _, ok := ut.(*types.FunctionType); ok {
		// Case 3.7: Function (Closure) Drop
		if isPointerInC {
			g.emit("if (%s && %s->drop_fn != NULL) {", expr, expr)
			g.emit("    %s->drop_fn(%s->env);", expr, expr)
			g.emit("}")
		} else {
			g.emit("if (%s.drop_fn != NULL) {", expr)
			g.emit("    %s.drop_fn(%s.env);", expr, expr)
			g.emit("}")
		}
	} else {
		// Case 4: Individual Drop
		dropMethod := g.getDropMethod(t)
		if dropMethod != "" {
			_, isAuto := g.AutoDropMethods[dropMethod]
			if isPointerInC {
				if isAuto {
					g.emit("%s(%s);", dropMethod, expr)
				} else {
					g.emit("%s(NULL, %s);", dropMethod, expr)
				}
			} else {
				if isAuto {
					g.emit("%s(&%s);", dropMethod, expr)
				} else {
					g.emit("%s(NULL, &%s);", dropMethod, expr)
				}
			}
		}
	}

	if g.isHeapAllocated(t) {
		if ut.Name() == "str" {
			g.emit("nr_free_str(%s);", expr)
		} else if _, ok := ut.(*types.ChanType); ok {
			g.emit("channel_free(%s);", expr)
		} else if _, ok := ut.(*types.MapType); ok {
			g.emit("map_free(%s);", expr)
		} else {
			g.emit("nr_free(%s);", expr)
		}
	}
}

func (g *Generator) requestAutoDrop(t types.NRType) string {
	name := "nr_drop_" + g.mangledTypeName(t)
	if _, ok := g.AutoDropMethods[name]; !ok {
		g.AutoDropMethods[name] = t
	}
	return name
}

func (g *Generator) emitAutoDropMethods() {
	generated := make(map[string]bool)
	for {
		added := false
		// Sort keys for deterministic output
		keys := make([]string, 0, len(g.AutoDropMethods))
		for k := range g.AutoDropMethods {
			keys = append(keys, k)
		}
		sort.Strings(keys)

		for _, name := range keys {
			if generated[name] {
				continue
			}
			generated[name] = true
			added = true

			t := g.AutoDropMethods[name]
			g.emit("void %s(%s* self) {", name, g.cType(t))
			g.emit("    if (self == NULL) return;")

			if st, ok := t.(*types.StructType); ok {
				for _, fName := range st.FieldNames {
					fType := st.Fields[fName]
					if types.IsOwnedType(fType) {
						fExpr := fmt.Sprintf("self->%s", fName)
						g.emitDrop(fExpr, fType, g.isPointerTypeInC(fType))
					}
				}
			} else if st, ok := t.(*types.SumType); ok {
				vNames := g.sortedVariantNames(st)
				emittedCount := 0
				for _, vName := range vNames {
					variant := st.Variants[vName]
					ownedFields := make([]string, 0)
					for fName, fType := range variant.Fields {
						if types.IsOwnedType(fType) {
							ownedFields = append(ownedFields, fName)
						}
					}
					if len(ownedFields) == 0 {
						continue
					}

					prefix := "if"
					if emittedCount > 0 {
						prefix = "else if"
					}
					g.emit("    %s (self->tag == %s_TAG_%s) {", prefix, g.cType(st), vName)
					for _, fName := range ownedFields {
						fType := variant.Fields[fName]
						var fExpr string
						if len(variant.Fields) == 1 {
							fExpr = fmt.Sprintf("self->data.%s", vName)
						} else {
							fExpr = fmt.Sprintf("self->data.%s.%s", vName, fName)
						}
						g.emitDrop(fExpr, fType, g.isPointerTypeInC(fType))
					}
					g.emit("    }")
					emittedCount++
				}
			}
			g.emit("}")
			g.emit("")
		}
		if !added {
			break
		}
	}
}

func (g *Generator) getDropMethod(t types.NRType) string {
	if t == nil {
		return ""
	}

	// Unwrap pointer for lookup (we want the methods on the base type)
	base := t
	if pt, ok := t.(*types.PointerType); ok {
		base = pt.Base
	}

	// Try to find the drop method in SemanticInfo
	// We check MethodSymbols which stores specialized and base methods.
	if methods, ok := g.SemanticInfo.MethodSymbols[base]; ok {
		if sym, ok := methods["drop"]; ok {
			return g.mangleName(sym)
		}
	}

	for k, methods := range g.SemanticInfo.MethodSymbols {
		if types.Equals(k, base) || (k.Name() != "" && k.Name() == base.Name()) {
			if sym, ok := methods["drop"]; ok {
				return g.mangleName(sym)
			}
		}
	}

	return ""
}

func (g *Generator) isDropMethodReceiverOwned(t types.NRType) bool {
	if t == nil {
		return false
	}
	base := t
	if pt, ok := t.(*types.PointerType); ok {
		base = pt.Base
	}

	var dropSym *semantic.Symbol
	if methods, ok := g.SemanticInfo.MethodSymbols[base]; ok {
		if sym, ok := methods["drop"]; ok {
			dropSym = sym
		}
	}
	if dropSym == nil {
		for k, methods := range g.SemanticInfo.MethodSymbols {
			if types.Equals(k, base) || (k.Name() != "" && k.Name() == base.Name()) {
				if sym, ok := methods["drop"]; ok {
					dropSym = sym
					break
				}
			}
		}
	}

	if dropSym != nil {
		if ft, ok := dropSym.Type.(*types.FunctionType); ok && ft.Receiver != nil {
			if pt, ok := ft.Receiver.(*types.PointerType); ok && pt.Kind == types.LeaseMove {
				return true
			}
		}
	}
	return false
}

func (g *Generator) getDropFunctionPointer(t types.NRType) string {
	if !types.IsOwnedType(t) {
		return "NULL"
	}

	ut := types.UnwrapLease(t)
	if pt, ok := ut.(*types.PointerType); ok && !pt.IsArray {
		ut = pt.Base
	}

	// Strings are handled specially in map_free if drop_fn is NULL,
	// but we can also pass a wrapper if we want. For now, NULL is fine for str.
	if ut.Name() == "str" {
		return "NULL"
	}

	dropMethod := g.getDropMethod(t)
	if dropMethod == "" {
		if st, ok := ut.(*types.StructType); ok && g.hasOwnedFields(st) {
			dropMethod = g.requestAutoDrop(st)
		} else if st, ok := ut.(*types.SumType); ok && g.hasOwnedSumFields(st) {
			dropMethod = g.requestAutoDrop(st)
		}
	}

	if dropMethod != "" {
		return dropMethod
	}
	return "NULL"
}

func (g *Generator) needsDrop(t types.NRType) bool {
	if t == nil {
		return false
	}
	ut := types.UnwrapLease(t)
	if ut.Name() == "str" {
		return true
	}
	if st, ok := ut.(*types.StructType); ok {
		return g.hasOwnedFields(st)
	}
	if sum, ok := ut.(*types.SumType); ok {
		return g.hasOwnedSumFields(sum)
	}
	k := ut.GetKind()
	return k == types.KindList || k == types.KindMap || k == types.KindChan || k == types.KindProtocol || k == types.KindFunction
}

func (g *Generator) hasDropFlag(sym *semantic.Symbol) bool {
	if sym == nil {
		return false
	}
	if g.CurrentLambda != nil {
		scope := g.SemanticInfo.Scopes[g.CurrentLambda]
		if scope != nil && scope.Captures != nil && scope.Captures[sym] {
			return false
		}
	}
	return (sym.Kind == semantic.SymVar || sym.Kind == semantic.SymParam) && types.IsOwnedType(sym.Type)
}

func (g *Generator) dropFlagName(sym *semantic.Symbol) string {
	return "nr_df_" + g.mangleName(sym)
}

func (g *Generator) variableName(sym *semantic.Symbol) string {
	if sym == nil {
		return ""
	}
	if g.CurrentLambda != nil && (sym.Kind == semantic.SymVar || sym.Kind == semantic.SymParam) {
		scope := g.SemanticInfo.Scopes[g.CurrentLambda]
		if scope != nil && scope.Captures != nil && scope.Captures[sym] {
			return fmt.Sprintf("_env->%s", g.mangleName(sym))
		}
	}
	return g.mangleName(sym)
}

func (g *Generator) requestAutoEq(t types.NRType) string {
	name := "nr_eq_" + g.mangledTypeName(t)
	if _, ok := g.AutoEqMethods[name]; !ok {
		g.AutoEqMethods[name] = t
	}
	return name
}

func (g *Generator) getEqMethod(t types.NRType) string {
	if t == nil {
		return ""
	}
	// Unwrap pointer for lookup (we want the methods on the base type)
	base := t
	if pt, ok := t.(*types.PointerType); ok {
		base = pt.Base
	}

	// 1. Try user-defined 'eq' method
	if methods, ok := g.SemanticInfo.MethodSymbols[base]; ok {
		if sym, ok := methods["eq"]; ok {
			return g.mangleName(sym)
		}
	}

	for k, methods := range g.SemanticInfo.MethodSymbols {
		if types.Equals(k, base) || (k.Name() != "" && k.Name() == base.Name()) {
			if sym, ok := methods["eq"]; ok {
				return g.mangleName(sym)
			}
		}
	}

	// 2. Fallback to auto-generated structural equality for structs
	if _, ok := base.(*types.StructType); ok {
		return g.requestAutoEq(base)
	}

	return ""
}

func (g *Generator) emitAutoEqMethods() {
	generated := make(map[string]bool)
	for {
		added := false
		keys := make([]string, 0, len(g.AutoEqMethods))
		for k := range g.AutoEqMethods {
			keys = append(keys, k)
		}
		sort.Strings(keys)

		for _, name := range keys {
			if generated[name] {
				continue
			}
			generated[name] = true
			added = true

			t := g.AutoEqMethods[name]
			g.emit("bool %s(%s* a, %s* b) {", name, g.cType(t), g.cType(t))
			g.emit("    if (a == b) return true;")
			g.emit("    if (a == NULL || b == NULL) return false;")

			if st, ok := t.(*types.StructType); ok {
				for _, fName := range st.FieldNames {
					fType := st.Fields[fName]
					ut := types.UnwrapLease(fType)

					eqCall := ""
					if _, isStruct := ut.(*types.StructType); isStruct {
						eqMethod := g.getEqMethod(ut)
						args := ""
						if !strings.HasPrefix(eqMethod, "nr_eq_") {
							args = "NULL, "
						}
						if g.isPointerTypeInC(fType) {
							eqCall = fmt.Sprintf("!%s(%sa->%s, b->%s)", eqMethod, args, fName, fName)
						} else {
							eqCall = fmt.Sprintf("!%s(%s&(a->%s), &(b->%s))", eqMethod, args, fName, fName)
						}
					} else if ut.Name() == "str" {
						eqCall = fmt.Sprintf("!nr_str_eq(a->%s, b->%s)", fName, fName)
					} else if pt, ok := ut.(*types.PointerType); ok && !pt.IsArray {
						// For pointers, we compare values if it's a struct pointer, else reference
						if _, isStruct := pt.Base.(*types.StructType); isStruct {
							eqMethod := g.getEqMethod(pt.Base)
							args := ""
							if !strings.HasPrefix(eqMethod, "nr_eq_") {
								args = "NULL, "
							}
							eqCall = fmt.Sprintf("!%s(%sa->%s, b->%s)", eqMethod, args, fName, fName)
						} else {
							eqCall = fmt.Sprintf("a->%s != b->%s", fName, fName)
						}
					} else if _, isFunc := ut.(*types.FunctionType); isFunc {
						eqCall = fmt.Sprintf("(a->%s.env != b->%s.env || a->%s.fn_ptr != b->%s.fn_ptr)", fName, fName, fName, fName)
					} else if _, isProto := ut.(*types.ProtocolType); isProto {
						if g.isPointerTypeInC(fType) {
							eqCall = fmt.Sprintf("(a->%s->data != b->%s->data || a->%s->vtable != b->%s->vtable)", fName, fName, fName, fName)
						} else {
							eqCall = fmt.Sprintf("(a->%s.data != b->%s.data || a->%s.vtable != b->%s.vtable)", fName, fName, fName, fName)
						}
					} else {
						eqCall = fmt.Sprintf("a->%s != b->%s", fName, fName)
					}
					g.emit("    if (%s) return false;", eqCall)
				}
			}
			g.emit("    return true;")
			g.emit("}")
			g.emit("")
		}
		if !added {
			break
		}
	}
}

func (g *Generator) emitGlobalInits() {
	g.emit("// --- GLOBAL INITIALIZERS ---")
	g.emit("void nr_init_globals(void) {")
	for _, s := range g.GlobalInits {
		sym := g.SemanticInfo.Defs[s.Name]
		if sym == nil {
			continue
		}
		name := g.mangleName(sym)
		g.emitLine(s)
		g.buf.WriteString(fmt.Sprintf("    %s = ", name))
		g.genExpression(s.Value)
		g.emit(";")
	}
	g.emit("}")
	g.currentFile = ""
	g.currentLine = -1
}

func (g *Generator) emitGlobalCleanups() {
	g.emit("void nr_cleanup_globals(void) {")
	for i := len(g.GlobalInits) - 1; i >= 0; i-- {
		s := g.GlobalInits[i]
		sym := g.SemanticInfo.Defs[s.Name]
		if sym == nil {
			continue
		}
		if types.IsOwnedType(sym.Type) {
			name := g.mangleName(sym)
			isPtr := g.isSymbolPointerInC(sym)
			g.emitDrop(name, sym.Type, isPtr)
		}
	}
	g.emit("}")
	g.emit("")
	g.emit("void nr_cleanup_globals(void);")
	g.emit("")
}

func (g *Generator) requestVTable(t types.NRType, p *types.ProtocolType) string {
	sanitizedName := t.Name()

	// Sanitize any characters that are invalid in C identifiers
	r := strings.NewReplacer(
		"@", "",
		"#", "",
		"&", "",
		"(", "_",
		")", "_",
		"[", "_",
		"]", "_",
		"*", "_ptr_",
		" ", "_",
		",", "_",
		".", "_",
	)
	sanitizedName = r.Replace(sanitizedName)
	sanitizedName = strings.Trim(sanitizedName, "_")

	key := fmt.Sprintf("%s_%s", sanitizedName, p.ProtocolName)
	if _, ok := g.VTables[key]; !ok {
		g.VTables[key] = VTableInstance{Type: t, Protocol: p}
	}
	return "vtable_" + key
}

func (g *Generator) emitVTables() {
	if len(g.VTables) == 0 {
		return
	}
	g.emit("// --- VTABLES ---")
	for key, inst := range g.VTables {
		vtableName := "vtable_" + key
		if g.MultiFileMode {
			g.emit("void* %s[] = {", vtableName)
		} else {
			g.emit("static void* %s[] = {", vtableName)
		}

		dropFn := g.getDropFunctionPointer(inst.Type)
		g.emit("    (void*)%s,", dropFn)

		// Sort method names to match protocol order (replicated from analyzer/prototypes)
		methodNames := []string{}
		for name := range inst.Protocol.Methods {
			methodNames = append(methodNames, name)
		}
		sort.Strings(methodNames)

		for _, mName := range methodNames {
			concreteMethod := g.findConcreteMethod(inst.Type, mName)
			g.emit("    (void*)%s,", concreteMethod)
		}
		g.emit("};")
		g.emit("")
	}
}

func (g *Generator) findConcreteMethod(t types.NRType, mName string) string {
	ut := types.UnwrapLease(t)

	// Check semantic info for monomorphized methods first
	if methods, ok := g.SemanticInfo.MethodSymbols[ut]; ok {
		if sym, ok := methods[mName]; ok {
			return g.mangleName(sym)
		}
	}

	for k, methods := range g.SemanticInfo.MethodSymbols {
		if types.Equals(k, ut) || (k.Name() != "" && k.Name() == ut.Name()) {
			if sym, ok := methods[mName]; ok {
				return g.mangleName(sym)
			}
		}
	}

	// Fallback for simple types or if symbol is not found
	sanitizedName := ut.Name()
	sanitizedName = strings.TrimPrefix(strings.TrimPrefix(strings.TrimPrefix(sanitizedName, "@"), "#"), "&")
	return sanitizedName + "_" + mName
}

func (g *Generator) registerStringLiteral(val string) string {
	if name, exists := g.StringLiterals[val]; exists {
		return name
	}
	name := fmt.Sprintf("_str_lit_%d", len(g.StringLiterals)+1)
	g.StringLiterals[val] = name
	return name
}

func (g *Generator) escapeStringForC(s string) string {
	var buf strings.Builder
	for i := 0; i < len(s); i++ {
		c := s[i]
		if c == '"' {
			if i > 0 && s[i-1] == '\\' {
				buf.WriteByte(c)
			} else {
				buf.WriteString(`\"`)
			}
		} else if c == '\n' {
			buf.WriteString(`\n`)
		} else if c == '\r' {
			buf.WriteString(`\r`)
		} else if c == '\t' {
			buf.WriteString(`\t`)
		} else {
			buf.WriteByte(c)
		}
	}
	return buf.String()
}

func (g *Generator) emitStringLiterals() {
	if len(g.StringLiterals) == 0 {
		return
	}

	type litItem struct {
		val  string
		name string
	}
	var items []litItem
	for val, name := range g.StringLiterals {
		items = append(items, litItem{val, name})
	}
	sort.Slice(items, func(i, j int) bool {
		return items[i].name < items[j].name
	})

	g.emit("// Global Static String Literals")
	for _, item := range items {
		escaped := g.escapeStringForC(item.val)
		size := len(item.val) + 1
		g.emit("static const struct { nr_header_t h; char data[%d]; } %s = { {(int32_t)%d, 1, NR_MAGIC_STATIC, 1, \"literal\", 0, NULL, NULL}, \"%s\" };", size, item.name, size-1, escaped)
	}
	g.emit("")
}

